<template>
  <div class="inline-block">
    <span
        :class="[
        textClasses[variant],
        sizeClasses[size],
        'font-medium p-1 transition-colors'
      ]"
    >
      {{ title }}
    </span>
  </div>
</template>

<script setup lang="ts">
import { computed } from 'vue'

interface Props {
  /** –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Ç–µ–∫—Å—Ç–∞ */
  title: string
  /** –¶–≤–µ—Ç–æ–≤–∞—è —Ç–µ–º–∞ */
  variant?: 'default' | 'primary' | 'muted'
  /** –†–∞–∑–º–µ—Ä —Ç–µ–∫—Å—Ç–∞ */
  size?: 'sm' | 'base' | 'xl' | '2xl'
}

const props = withDefaults(defineProps<Props>(), {
  variant: 'default',
  size: 'xl'
})

const textClasses = {
  default: 'text-gray-900 dark:text-gray-100',
  primary: 'text-primary-600 dark:text-primary-400',
  muted: 'text-gray-500 dark:text-gray-400'
}

const sizeClasses = {
  sm: 'text-sm',
  base: 'text-base',
  xl: 'text-xl',
  '2xl': 'text-2xl'
}

// üí° –í—ã—á–∏—Å–ª—è–µ–º –æ–±—ä–µ–¥–∏–Ω—ë–Ω–Ω—ã–π –∫–ª–∞—Å—Å
const computedClasses = computed(() => {
  return [
    textClasses[props.variant],
    sizeClasses[props.size],
    'font-medium p-1 transition-colors'
  ].join(' ')
})
</script>

<style scoped>
/* –ü–ª–∞–≤–Ω–æ–µ –∏–∑–º–µ–Ω–µ–Ω–∏–µ —Ü–≤–µ—Ç–∞ –ø—Ä–∏ –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–∏ —Ç–µ–º—ã */
span {
  transition: color 0.2s ease;
}
</style>
